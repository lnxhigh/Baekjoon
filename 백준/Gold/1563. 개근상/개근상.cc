#include <bits/stdc++.h>
using namespace std;

const int MOD = 1000000;
const int MAX = 1 << 10;
int D[MAX][2][2][2]; // D[i][지각][결석][결석]

int main() {
    int N; cin >> N;
    D[1][0][0][0] = D[1][1][0][0] = D[1][0][1][0] = 1;

    for (int i = 2; i <= N; i++) {

        D[i][0][0][0] = D[i-1][0][0][0] + D[i-1][0][0][1];
        D[i][0][0][1] = D[i-1][0][1][0] + D[i-1][0][1][1];
        D[i][0][1][0] = D[i-1][0][0][0] + D[i-1][0][0][1];
        D[i][0][1][1] = D[i-1][0][1][0];
        
        D[i][1][0][0] = D[i-1][0][0][0] + D[i-1][0][0][1]+ D[i-1][1][0][0] + D[i-1][1][0][1];
        D[i][1][0][1] = D[i-1][0][1][0] + D[i-1][0][1][1] + D[i-1][1][1][0] + D[i-1][1][1][1];
        D[i][1][1][0] = D[i-1][1][0][0] + D[i-1][1][0][1];
        D[i][1][1][1] = D[i-1][1][1][0];

        for (int k = 0; k < (1 << 3); k++) {
            D[i][k >> 0 & 1][k >> 1 & 1][k >> 2 & 1] %= MOD;
        }
    }

    int res = 0;
    for (int i = 0; i < (1 << 3); i++) {
        res += D[N][i >> 0 & 1][i >> 1 & 1][i >> 2 & 1];
    }
    res %= MOD;
    cout << res << '\n';
    
    return 0;
}
